def _worker(worker_id: int, split: str, start_idx: int, count: int,
                 args, output_base_str: str, seed: int,
                 level: Optional[int] = None,
                 level_subdir: Optional[str] = None) -> Tuple[dict, list]:
    """One worker process: generates `count` instances into the split.

    ``level`` (0..5) filters the template sampler; ``level_subdir``
    nests output under ``<output_dir>/<split>/<subdir>``.  Both are
    None for the legacy mixed-train flow.
    """
    rng = np.random.default_rng(seed + worker_id * 9999)
    output_base = Path(output_base_str)
    cfg = MultilevelBlocksConfig(n_cubes=20, n_oblong=12, n_long=6)
    render_tasks: list = []
    use_full = level in _FULL_EXECUTOR_LEVELS
    with tempfile.TemporaryDirectory(prefix="mlb_data_") as td:
        builder, ws, cfg = make_multilevel_blocks_builder(
            scratch_dir=Path(td), config=cfg)
        env = builder.build_env(rate=10000.0)
        _maybe_disable_hand_capsule(env, args)
        executor = _make_executor(env, ws, cfg, fast=not use_full)

        stats = _init_stats()
        config_num = start_idx
        # 60× retry budget per accepted problem.  Was 30; bumped so L5
        # upright templates (compound w/ right-half upright sub or
        # double_bridges with high ix), which reject ~91 % of the time
        # due to LUT-cached-posture clipping at boundary stack cells,
        # still get enough samples through to fill the per-level quota.
        max_attempts = count * 60  # safety cap so an impossible level
                                          # can't loop forever
        while stats["accepted"] < count and stats["attempts"] < max_attempts:
            stats["attempts"] += 1
            try:
                res = _generate_one(
                    config_num, rng, args, env, ws, cfg, executor,
                    output_base, split, render_now=False,
                    level=level, level_subdir=level_subdir,
                )
            except Exception:
                stats["reject_plan_failed"] += 1
                continue
            if res is None:
                stats["reject_plan_failed"] += 1
                continue
            stats["accepted"] += 1
            stats["templates"][res["template"]] += 1
            stats["plan_len_hist"][res["plan_len"]] += 1
            stats["gen_seconds"].append(res["elapsed_s"])
            if not args.no_viz:
                render_tasks.append(
                    (res["config_num"], res["viz_path"],
                          res["template_obj"]))
            config_num += 1
    return stats, render_tasks


def _generate_split_parallel(
    split: str, count: int, args,
    output_base: Path, seed: int,
    *,
    level: Optional[int] = None,
    level_subdir: Optional[str] = None,
    config_offset: int = 0,
) -> dict:
    """Multi-worker split runner.

    Optional ``level`` filters templates; ``level_subdir`` nests output
    under ``<split>/<subdir>``.  ``config_offset`` shifts the starting
    config_num so multiple level chunks under the same split don't
    collide on filenames (used when generating mixed-train across
    levels in sequence).
    """
    if count <= 0:
        return _init_stats()
    n_workers = args.num_workers
    per_worker = count // n_workers
    remainder = count - per_worker * n_workers
    chunks = []
    cursor = 1 + config_offset
    for w in range(n_workers):
        c = per_worker + (1 if w < remainder else 0)
        chunks.append((w, split, cursor, c, args, str(output_base), seed,
                            level, level_subdir))
        cursor += c

    ctx = mp.get_context("fork")
    with ctx.Pool(n_workers) as pool:
        results = pool.starmap(_worker, chunks)

    # Merge stats and collect render tasks.
    merged = _init_stats()
    all_render: list = []
    for s, render_tasks in results:
        merged["attempts"] += s["attempts"]
        merged["accepted"] += s["accepted"]
        merged["reject_plan_failed"] += s["reject_plan_failed"]
        merged["templates"].update(s["templates"])
        merged["plan_len_hist"].update(s["plan_len_hist"])
        merged["gen_seconds"].extend(s["gen_seconds"])
        all_render.extend(render_tasks)

    # Serial viz pass in main process.
    if all_render:
        all_render.sort(key=lambda t: t[0])   # by config_num
        print(f"  [{split}] rendering {len(all_render)} viz "
                  f"(serial, post-pool)...")
        cfg = MultilevelBlocksConfig(n_cubes=20, n_oblong=12, n_long=6)
        with tempfile.TemporaryDirectory(prefix="mlb_viz_") as td:
            builder, ws, cfg = make_multilevel_blocks_builder(
                scratch_dir=Path(td), config=cfg)
            env = builder.build_env(rate=10000.0)
            _maybe_disable_hand_capsule(env, args)
            t_viz = time.time()
            for config_num, viz_path, template in all_render:
                try:
                    render_template_pair(template, ws, cfg, env,
                                                Path(viz_path))
                except Exception as exc:
                    print(f"    viz {config_num} failed: "
                              f"{type(exc).__name__}: {exc}")
            print(f"  [{split}] viz done in {time.time() - t_viz:.1f}s")

    return merged


