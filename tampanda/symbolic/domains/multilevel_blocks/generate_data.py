def _safe_iy(rng: np.random.Generator, cfg: MultilevelBlocksConfig,
                 *, max_iy_cap: int = 3) -> int:
    """Sample iy biased toward small values (front of stack table).

    put_upright has IK reach limits at high iy on the default stack
    grid; templates with upright actions should clamp here.  For non-
    upright templates the cap is ignored.
    """
    cap = min(max_iy_cap, cfg.stack_grid_cells[1] - 4)
    return int(rng.integers(0, cap + 1))


def _maybe_disable_hand_capsule(env, args) -> None:
    """Disable the Franka hand_capsule bounding-capsule collision geom.

    The 4cm-radius × 6cm-half-length capsule is a conservative bounding
    approximation; its 10cm axial extent over-rejects feasible plans in
    dense structures (upright_bridges, tower_on_bridge), in particular
    failing the put_upright settle-to-traverse IK at z=0.50.  Real
    collisions are still checked via the hand_c mesh.
    """
    if getattr(args, "enable_hand_capsule", False):
        return
    for gid in range(env.model.ngeom):
        if env.model.geom(gid).name == "hand_capsule":
            env.model.geom_contype[gid] = 0
            env.model.geom_conaffinity[gid] = 0
            break


def _sample_template(
    rng: np.random.Generator,
    cfg: MultilevelBlocksConfig,
    allowed: Sequence[str],
    *,
    level: Optional[int] = None,
) -> Template:
    """Sample a template from ``allowed``.

    If ``level`` is given, the parameter ranges are biased to produce
    plans of the right difficulty (e.g., L1 only uses cube_tower with
    h=2..3; L3 forces orient-change in oblong_tower).
    """
    choice = str(rng.choice(allowed))
    cells_x, cells_y, cells_z = cfg.stack_grid_cells

    if choice == "cube_pick_put":
        ix = int(rng.integers(1, cells_x - 1))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return cube_pick_put(ix, iy, cfg=cfg)

    if choice == "simple_long_x":
        # 3-cell long block along x — needs ix..ix+2 to fit.  Bias to
        # left-half (ix < 5) so put-long-x stays in the proven-feasible
        # range; right-half (ix >= 5) still allowed but biased low.
        if rng.random() < 0.85:
            ix = int(rng.integers(1, 4))   # ix ∈ {1, 2, 3}
        else:
            ix = int(rng.integers(4, cells_x - 3))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_long_x(ix, iy, cfg=cfg)

    if choice == "simple_upright_pickput":
        # Single upright oblong at (ix, iy) spanning L0+L1.  Bias toward
        # left-half ix where put-upright is reliable in fast mode.
        if rng.random() < 0.85:
            ix = int(rng.integers(1, 5))
        else:
            ix = int(rng.integers(5, cells_x - 1))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_upright_pickput(ix, iy, cfg=cfg)

    if choice == "simple_long_rotate":
        # Long block placed flat-y along (ix, iy..iy+2).  Bias toward
        # left-half ix and lower iy so the descent has room.
        if rng.random() < 0.85:
            ix = int(rng.integers(1, 5))
        else:
            ix = int(rng.integers(5, cells_x - 1))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 4)
        return simple_long_rotate(ix, iy, cfg=cfg)

    # ---- Extended primitive-exposure templates ----
    # Helper to pick a left-biased ix (85 % left-half).
    def _biased_ix(low: int, high: int, mid: int = 5) -> int:
        if rng.random() < 0.85 and mid > low:
            return int(rng.integers(low, min(mid, high)))
        return int(rng.integers(low, high))

    if choice == "simple_oblong_flat_x":
        ix = _biased_ix(1, cells_x - 2)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_oblong_flat_x(ix, iy, cfg=cfg)

    if choice == "simple_oblong_flat_y":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 3)
        return simple_oblong_flat_y(ix, iy, cfg=cfg)

    if choice == "simple_long_y":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 4)
        return simple_long_y(ix, iy, cfg=cfg)

    if choice == "simple_pick_upright_then_put_upright":
        # Source upright at (ix, iy), goal upright at different (ix2, iy2)
        # — both anchors left-biased; ensure they don't overlap.
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        ix2 = _biased_ix(1, cells_x - 1)
        iy2 = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        if (ix, iy) == (ix2, iy2):
            ix2 = (ix2 + 1) % (cells_x - 1) or 1
        return simple_pick_upright_then_put_upright(
            ix, iy, ix2, iy2, cfg=cfg,
        )

    if choice == "simple_pick_long_upright_then_put_long_upright":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        ix2 = _biased_ix(1, cells_x - 1)
        iy2 = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        if (ix, iy) == (ix2, iy2):
            ix2 = (ix2 + 1) % (cells_x - 1) or 1
        return simple_pick_long_upright_then_put_long_upright(
            ix, iy, ix2, iy2, cfg=cfg,
        )

    if choice == "simple_oblong_rotate_y_to_x":
        ix = _biased_ix(1, cells_x - 2)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_oblong_rotate_y_to_x(ix, iy, cfg=cfg)

    if choice == "simple_long_rotate_y_to_x":
        ix = _biased_ix(1, cells_x - 3)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_long_rotate_y_to_x(ix, iy, cfg=cfg)

    if choice == "simple_make_upright_from_y":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 3)
        return simple_make_upright_from_y(ix, iy, cfg=cfg)

    if choice == "simple_make_long_upright_from_x":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_make_long_upright_from_x(ix, iy, cfg=cfg)

    if choice == "simple_make_long_upright_from_y":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 4)
        return simple_make_long_upright_from_y(ix, iy, cfg=cfg)

    if choice == "simple_unstack_oblong_to_flat_x":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        ix2 = _biased_ix(1, cells_x - 2)
        iy2 = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_unstack_oblong_to_flat_x(
            ix, iy, ix2, iy2, cfg=cfg,
        )

    if choice == "simple_unstack_oblong_to_flat_y":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        ix2 = _biased_ix(1, cells_x - 1)
        iy2 = _safe_iy(rng, cfg, max_iy_cap=cells_y - 3)
        return simple_unstack_oblong_to_flat_y(
            ix, iy, ix2, iy2, cfg=cfg,
        )

    if choice == "simple_unstack_long_to_flat_x":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        ix2 = _biased_ix(1, cells_x - 3)
        iy2 = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return simple_unstack_long_to_flat_x(
            ix, iy, ix2, iy2, cfg=cfg,
        )

    if choice == "simple_unstack_long_to_flat_y":
        ix = _biased_ix(1, cells_x - 1)
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        ix2 = _biased_ix(1, cells_x - 1)
        iy2 = _safe_iy(rng, cfg, max_iy_cap=cells_y - 4)
        return simple_unstack_long_to_flat_y(
            ix, iy, ix2, iy2, cfg=cfg,
        )

    if choice == "cube_tower":
        if level is not None and level <= 1:
            h = int(rng.integers(2, min(cells_z, 3) + 1))
        elif level is not None and level == 3:
            h = int(rng.integers(4, min(cells_z, 4) + 1))  # tall only
        else:
            h = int(rng.integers(2, min(cells_z, 4) + 1))
        ix = int(rng.integers(1, cells_x - 1))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return cube_tower(h, ix, iy, cfg=cfg)

    if choice == "oblong_tower":
        h = int(rng.integers(2, min(cells_z, 3) + 1))
        ix = int(rng.integers(1, cells_x - 3))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        if level is not None and level == 3:
            # L3 must have orient change → flat target opposite of source.
            flat = "flat-y"   # source is always flat-x → triggers turn-x-to-y
        elif level is not None and level == 2:
            flat = "flat-x"   # same as source → no transform
        else:
            flat = str(rng.choice(["flat-x", "flat-y"]))
        return oblong_tower(h, ix, iy, cfg=cfg, flat_orientation=flat)

    if choice == "long_pyramid":
        ix = int(rng.integers(1, cells_x - 3))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return long_pyramid(ix, iy, cfg=cfg)

    if choice == "upright_bridges":
        ix = int(rng.integers(1, cells_x - 4))
        iy = _safe_iy(rng, cfg)   # tight: only front-half iy
        return upright_bridges(ix, iy, cfg=cfg)

    if choice == "tower_on_bridge":
        ix = int(rng.integers(1, cells_x - 4))
        iy = _safe_iy(rng, cfg)
        tower_h = int(rng.integers(1, 3))   # 1 or 2 cubes on top
        return tower_on_bridge(ix, iy, cfg=cfg, tower_h=tower_h)

    if choice == "multi_tower":
        n = int(rng.integers(2, 5))   # 2..4 towers
        return multi_tower(rng, cfg=cfg, n_towers=n,
                                height_range=(2, 4))

    if choice == "staircase":
        n = int(rng.integers(3, 5))   # 3 or 4 steps
        ix = int(rng.integers(0, max(1, cells_x - 3 * n)))
        iy = _safe_iy(rng, cfg, max_iy_cap=cells_y - 2)
        return staircase(ix, iy, cfg=cfg, n=n)

    if choice == "double_bridges":
        if cells_x < 8:
            # Not enough room → fall back to single bridge.
            ix = int(rng.integers(1, cells_x - 4))
            iy = _safe_iy(rng, cfg)
            return upright_bridges(ix, iy, cfg=cfg)
        # Bias ix strongly toward 0 — the second bridge sits at ix+4,
        # so ix=0 puts both bridges in the proven-feasible left-half
        # ix∈[0,4].  Higher ix puts the second bridge at the ix=6+
        # boundary where the fast executor's cached IK posture clips.
        # Let some right-half samples slip through (10%) so the dataset
        # still has variety; rejected ones get retried.
        r = float(rng.random())
        if r < 0.70:
            ix = 0
        elif r < 0.90:
            ix = 1
        else:
            ix = int(rng.integers(2, cells_x - 7))
        iy = _safe_iy(rng, cfg)
        return double_bridges(ix, iy, cfg=cfg)

    if choice == "compound":
        return compound(rng, cfg=cfg,
                              n_substructures=int(rng.integers(2, 4)))

    if choice == "random_mix":
        return random_mix(rng, cfg=cfg)
    raise ValueError(f"unknown template: {choice}")


def _count_blocks_needed(template: Template) -> Dict[str, int]:
    """Return ``{shape: count}`` needed by template."""
    out: Dict[str, int] = {"cube": 0, "oblong": 0, "long": 0}
    for block, _, _ in template.source_placements:
        out[_block_shape(block)] += 1
    return out


def _make_cfg_for_template(template: Template, *,
                                base: Optional[MultilevelBlocksConfig] = None
                                ) -> MultilevelBlocksConfig:
    """Build a config with enough blocks for the template."""
    needs = _count_blocks_needed(template)
    if base is None:
        base = MultilevelBlocksConfig()
    kwargs = asdict(base)
    kwargs["n_cubes"] = max(kwargs.get("n_cubes", 0), needs["cube"])
    kwargs["n_oblong"] = max(kwargs.get("n_oblong", 0), needs["oblong"])
    kwargs["n_long"] = max(kwargs.get("n_long", 0), needs["long"])
    return MultilevelBlocksConfig(**kwargs)


def _generate_one(
    config_num: int,
    rng: np.random.Generator,
    args,
    env,
    workspace,
    cfg: MultilevelBlocksConfig,
    executor,
    output_dir: Path,
    split: str,
    render_now: bool = True,
    level: Optional[int] = None,
    level_subdir: Optional[str] = None,
) -> Optional[Dict[str, Any]]:
    """Generate a single instance.

    If ``level`` is provided, the sampler picks only templates from
    ``_LEVELS[level]`` and tags the plan metadata with the level.  If
    ``level_subdir`` is set, output goes to ``<output_dir>/<split>/<sub>``
    instead of ``<output_dir>/<split>``.
    """
    if level is not None:
        allowed = tuple(_LEVELS[level])
    else:
        allowed = tuple(args.templates)
    template = _sample_template(rng, cfg, allowed, level=level)

    # Build the plan from the template.
    plan = build_plan(template)

    # Validate.  L4 (put_upright templates) uses the FULL executor —
    # the FAST variant has unresolved IK basin bugs in put_upright's
    # lift-back stage.  Other levels use FAST (~10x faster).
    init_state = _state_from_placements(template.source_placements)
    use_fast = level not in _FULL_EXECUTOR_LEVELS
    seq_res = check_action_sequence(
        env, workspace, cfg, init_state, plan,
        fast=use_fast, short_circuit=True, executor=executor,
        # Per-action restore: judge each action on a freshly-restored
        # scene reflecting the symbolic state, matching training-time
        # ``check_action`` semantics.  Avoids the cumulative drift
        # where sequential ``dispatch_action`` calls leave blocks
        # ~30 mm off rest, eventually triggering spurious collision
        # rejections on downstream upright placements (compound /
        # double_bridges L5 templates).
        per_action_restore=True,
    )
    if not seq_res["success"]:
        return None

    # Write outputs.  level_subdir nests under split (e.g., val/L0/).
    if level_subdir is not None:
        problem_dir = output_dir / split / level_subdir
    else:
        problem_dir = output_dir / split
    viz_dir = problem_dir / "viz"
    problem_dir.mkdir(parents=True, exist_ok=True)
    if not args.no_viz:
        viz_dir.mkdir(parents=True, exist_ok=True)

    problem_path = problem_dir / f"config_{config_num}.pddl"
    plan_path = problem_dir / f"config_{config_num}.pddl.plan"
    viz_path = viz_dir / f"config_{config_num}.png"

    pn_sub = level_subdir + "-" if level_subdir else ""
    write_pddl_problem(
        template, workspace, cfg, problem_path,
        problem_name=f"multilevel-{split}-{pn_sub}{config_num}",
    )
    difficulty = template.metadata.get("difficulty", level)
    write_plan_file(plan, plan_path, metadata={
        "template": template.name,
        "difficulty": difficulty,
        "split": split,
        "config_num": config_num,
        "elapsed_s": f"{seq_res['elapsed_s']:.2f}",
    })
    if render_now and not args.no_viz:
        render_template_pair(template, workspace, cfg, env, viz_path)

    return {
        "template": template.name,
        "difficulty": difficulty,
        "plan_len": len(plan),
        "elapsed_s": seq_res["elapsed_s"],
        "config_num": config_num,
        "split": split,
        "template_obj": template,
        "viz_path": str(viz_path),
    }


def _init_stats() -> dict:
    return {
        "attempts": 0, "accepted": 0,
        "reject_plan_failed": 0,
        "templates": Counter(),
        "plan_len_hist": Counter(),
        "gen_seconds": [],
    }


def _generate_split(
    split: str,
    count: int,
    args,
    output_base: Path,
    seed: int,
    *,
    level: Optional[int] = None,
    level_subdir: Optional[str] = None,
    config_offset: int = 0,
) -> dict:
    """Single-process split runner."""
    if count <= 0:
        return _init_stats()
    rng = np.random.default_rng(seed)

    cfg = MultilevelBlocksConfig(n_cubes=20, n_oblong=12, n_long=6)
    use_full = level in _FULL_EXECUTOR_LEVELS
    with tempfile.TemporaryDirectory(prefix="mlb_data_") as td:
        builder, ws, cfg = make_multilevel_blocks_builder(
            scratch_dir=Path(td), config=cfg)
        env = builder.build_env(rate=10000.0)
        _maybe_disable_hand_capsule(env, args)
        executor = _make_executor(env, ws, cfg, fast=not use_full)

        stats = _init_stats()
        t_start = time.time()
        config_num = config_offset
        # 60× retry budget per accepted problem.  Was 30; bumped so L5
        # upright templates (compound w/ right-half upright sub or
        # double_bridges with high ix), which reject ~91 % of the time
        # due to LUT-cached-posture clipping at boundary stack cells,
        # still get enough samples through to fill the per-level quota.
        max_attempts = count * 60
        while stats["accepted"] < count and stats["attempts"] < max_attempts:
            stats["attempts"] += 1
            config_num += 1
            try:
                res = _generate_one(
                    config_num, rng, args, env, ws, cfg,
                    executor, output_base, split,
                    level=level, level_subdir=level_subdir,
                )
            except Exception as exc:
                stats["reject_plan_failed"] += 1
                if args.verbose:
                    print(f"  [{split}] attempt {stats['attempts']} "
                              f"raised {type(exc).__name__}: {exc}")
                continue
            if res is None:
                stats["reject_plan_failed"] += 1
                continue

            stats["accepted"] += 1
            stats["templates"][res["template"]] += 1
            stats["plan_len_hist"][res["plan_len"]] += 1
            stats["gen_seconds"].append(res["elapsed_s"])

            if stats["accepted"] % 10 == 0 or stats["accepted"] == 1:
                elapsed = time.time() - t_start
                eta = elapsed / stats["accepted"] * (count - stats["accepted"])
                print(f"  [{split}] {stats['accepted']}/{count}  "
                          f"plan_len={res['plan_len']}  "
                          f"elapsed={elapsed:.0f}s  eta={eta:.0f}s  "
                          f"accept={stats['accepted']}/"
                          f"{stats['attempts']}")
    return stats


