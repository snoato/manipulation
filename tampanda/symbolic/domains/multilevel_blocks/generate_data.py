"""Multilevel-blocks dataset generation — PDDL problems with feasibility
validation.

For each generated instance:

1. A buildable structure template is sampled (cube_tower /
   oblong_tower / upright_bridges / long_pyramid).
2. The initial state (all source blocks on the parts table) and the
   goal state (target structure on the stack table) are derived from
   the template.
3. A plan is generated by reverse construction: for each block in
   dependency-respecting build order, the script emits
   ``pick → [in-hand transforms] → put`` actions.
4. Every action is validated via :func:`check_action_sequence` with
   the FAST executor.  Instances whose plan fails partway are
   rejected (the failed action's error is logged for debugging).
5. PDDL problem + plan + visualisation are saved.

Output layout mirrors rgnet's ``data/pddl_domains/example/blocks/``::

    data/multilevel_blocks/
    ├── domain.pddl
    ├── train/
    │   ├── config_1.pddl
    │   ├── config_1.pddl.plan
    │   ├── viz/
    │   │   └── config_1.png
    │   └── ...
    ├── test/
    └── val/

Usage::

    python -m tampanda.symbolic.domains.multilevel_blocks.generate_data
    python -m tampanda.symbolic.domains.multilevel_blocks.generate_data \\
        --num-train 200 --num-test 20 --num-val 20 \\
        --templates cube_tower oblong_tower upright_bridges long_pyramid \\
        --num-workers 4

CLI flags mirror tabletop/generate_data.py.
"""
