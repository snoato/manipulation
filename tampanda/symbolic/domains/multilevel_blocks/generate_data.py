def _setup_camera(cfg: MultilevelBlocksConfig, kind: str = "stack"
                    ) -> "mujoco.MjvCamera":
    """Camera presets.

    ``stack`` — close, looking at the stack table from the robot's
    front-right, top-down angle.  Used for goal snapshots.

    ``parts`` — close, looking at the parts table from behind the
    robot.  Used for initial-kit snapshots.  Same elevation as stack
    so the two side-by-side renders look symmetric.
    """
    cam = mujoco.MjvCamera()
    sp = cfg.stack_table_pos
    pp = cfg.parts_table_pos
    if kind == "stack":
        cam.azimuth = 180   # look from -y toward stack table at +y
        cam.elevation = -35
        cam.distance = 0.8
        cam.lookat[:] = [sp[0], sp[1], sp[2] + 0.18]
    else:   # parts
        cam.azimuth = 0     # look from +y toward parts table at -y
        cam.elevation = -35
        cam.distance = 0.8
        cam.lookat[:] = [pp[0], pp[1], pp[2] + 0.05]
    return cam


def render_template_pair(
    template: Template,
    workspace,
    cfg: MultilevelBlocksConfig,
    env,
    output_path: Path,
) -> None:
    """Render side-by-side PNG: initial state (on parts) + goal (on stack).

    Two snapshots concatenated horizontally.  Uses a wide camera that
    captures both tables.
    """
    if mujoco is None:
        return

    env.model.vis.global_.offwidth = max(1280, env.model.vis.global_.offwidth)
    env.model.vis.global_.offheight = max(720, env.model.vis.global_.offheight)
    renderer = mujoco.Renderer(env.model, height=720, width=640)
    cam_parts = _setup_camera(cfg, kind="parts")
    cam_stack = _setup_camera(cfg, kind="stack")

    # Initial state: all blocks at source cells.  Use the parts camera
    # so the kit is visible up close.
    init_state = _state_from_placements(template.source_placements)
    restore_state(env, workspace, cfg, init_state)
    renderer.update_scene(env.data, camera=cam_parts)
    img_init = renderer.render()

    # Final state: all blocks at goal cells.  Use the stack camera.
    goal_state = _state_from_placements(template.goal_placements)
    restore_state(env, workspace, cfg, goal_state)
    renderer.update_scene(env.data, camera=cam_stack)
    img_goal = renderer.render()

    # Concatenate horizontally with a thin separator.
    sep = np.full((img_init.shape[0], 8, 3), 32, dtype=img_init.dtype)
    img = np.concatenate([img_init, sep, img_goal], axis=1)

    try:
        import cv2  # type: ignore
        cv2.imwrite(str(output_path), img[:, :, ::-1])
    except ImportError:
        try:
            import matplotlib.pyplot as plt  # type: ignore
            plt.imsave(str(output_path), img)
        except ImportError:
            pass
    finally:
        renderer.close()


def _state_from_placements(
    placements: List[Tuple[str, List[str], str]],
) -> Dict[Tuple, bool]:
    """Build a minimal state dict containing just the (in ...) predicates."""
    state: Dict[Tuple, bool] = {}
    for block, cells, _ in placements:
        for c in cells:
            state[("in", block, c)] = True
    return state


