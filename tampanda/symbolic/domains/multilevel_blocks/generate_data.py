def _sort_cells_for_orientation(cells: List[str], orientation: str
                                   ) -> List[str]:
    """Sort cell IDs into the canonical order each PDDL action expects.

    * flat-x / long-x: ascending ix.
    * flat-y / long-y: ascending iy.
    * upright (oblong 2-cell) / long-upright (3-cell): ascending region
      (so L0 < L1 < L2).
    """
    from tampanda.symbolic.workspace import Cell
    parsed = [Cell.parse(c) for c in cells]
    if orientation in ("flat-x", "long-x"):
        parsed.sort(key=lambda c: c.ix)
    elif orientation in ("flat-y", "long-y"):
        parsed.sort(key=lambda c: c.iy)
    elif orientation in ("upright", "upright-2", "long-upright"):
        parsed.sort(key=lambda c: c.region)
    return [c.id for c in parsed]


def _action_for_pick(shape: str, orientation: str,
                          block: str, cells: List[str]) -> Tuple:
    """Build the PDDL pick action tuple for (shape, orientation, cells)."""
    sorted_cells = _sort_cells_for_orientation(cells, orientation)
    if shape == "cube":
        if len(sorted_cells) != 1:
            raise ValueError("cube must have 1 cell")
        return ("pick-cube", block, sorted_cells[0])
    if shape == "oblong":
        if orientation == "flat-x":
            return ("pick-flat-x", block, sorted_cells[0], sorted_cells[1])
        if orientation == "flat-y":
            return ("pick-flat-y", block, sorted_cells[0], sorted_cells[1])
        if orientation == "upright":
            return ("pick-upright", block, sorted_cells[0], sorted_cells[1])
    if shape == "long":
        if orientation == "flat-x":
            return ("pick-long-x", block, *sorted_cells)
        if orientation == "flat-y":
            return ("pick-long-y", block, *sorted_cells)
        if orientation == "upright":
            return ("pick-long-upright", block, *sorted_cells)
    raise ValueError(f"unsupported (shape, orientation): {shape, orientation}")


def _action_for_put(shape: str, orientation: str,
                         block: str, cells: List[str]) -> Tuple:
    """Build the PDDL put action tuple — same signature as pick.

    Note: put-cube/flat-*/upright follow the same naming pattern as
    pick-*, just with ``put-`` prefix.
    """
    sorted_cells = _sort_cells_for_orientation(cells, orientation)
    if shape == "cube":
        return ("put-cube", block, sorted_cells[0])
    if shape == "oblong":
        if orientation == "flat-x":
            return ("put-flat-x", block, sorted_cells[0], sorted_cells[1])
        if orientation == "flat-y":
            return ("put-flat-y", block, sorted_cells[0], sorted_cells[1])
        if orientation == "upright":
            return ("put-upright", block, sorted_cells[0], sorted_cells[1])
    if shape == "long":
        if orientation == "flat-x":
            return ("put-long-x", block, *sorted_cells)
        if orientation == "flat-y":
            return ("put-long-y", block, *sorted_cells)
        if orientation == "upright":
            return ("put-long-upright", block, *sorted_cells)
    raise ValueError(f"unsupported (shape, orientation): {shape, orientation}")


def _transform_actions(shape: str, src_orient: str, dst_orient: str,
                            block: str) -> List[Tuple]:
    """Emit the chain of in-hand transform actions to go from src to dst."""
    if src_orient == dst_orient:
        return []
    if shape == "cube":
        if src_orient != dst_orient:
            raise ValueError("cubes have no orientations")
        return []
    long_prefix = "-long" if shape == "long" else ""
    # Two-step transforms (e.g., upright → flat-x → flat-y) might be
    # needed; we shortcut via the direct PDDL action where possible.
    direct = {
        ("flat-x", "flat-y"): f"turn{long_prefix}-x-to-y",
        ("flat-y", "flat-x"): f"turn{long_prefix}-y-to-x",
        ("flat-x", "upright"): f"make{long_prefix}-upright-from-x",
        ("flat-y", "upright"): f"make{long_prefix}-upright-from-y",
        ("upright", "flat-x"): f"make{long_prefix}-flat-x-from-upright",
        ("upright", "flat-y"): f"make{long_prefix}-flat-y-from-upright",
    }
    key = (src_orient, dst_orient)
    if key in direct:
        return [(direct[key], block)]
    raise ValueError(
        f"no direct transform from {src_orient!r} to {dst_orient!r}; "
        "two-step transforms not yet implemented"
    )


def _block_shape(block_name: str) -> str:
    if block_name.startswith("cube_"):
        return "cube"
    if block_name.startswith("oblong_"):
        return "oblong"
    if block_name.startswith("long_"):
        return "long"
    raise ValueError(f"unknown block name: {block_name}")


def build_plan(template: Template) -> List[Tuple]:
    """Generate the action sequence that builds ``template``.

    Walks ``template.build_order`` and, for each block, emits:
      pick(source) → [transforms] → put(target)
    """
    src_by_block = {b: (cells, orient)
                       for b, cells, orient in template.source_placements}
    goal_by_block = {b: (cells, orient)
                          for b, cells, orient in template.goal_placements}

    plan: List[Tuple] = []
    for block in template.build_order:
        s_cells, s_orient = src_by_block[block]
        g_cells, g_orient = goal_by_block[block]
        shape = _block_shape(block)
        plan.append(_action_for_pick(shape, s_orient, block, s_cells))
        plan.extend(_transform_actions(shape, s_orient, g_orient, block))
        plan.append(_action_for_put(shape, g_orient, block, g_cells))
    return plan


