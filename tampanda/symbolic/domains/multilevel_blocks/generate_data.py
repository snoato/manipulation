def _print_stats(split: str, stats: dict, elapsed: float) -> None:
    print(f"\n=== {split} summary ===")
    print(f"  accepted/attempts: {stats['accepted']}/{stats['attempts']}")
    if stats['attempts']:
        print(f"  reject rate: {100*stats['reject_plan_failed']/stats['attempts']:.1f}%")
    print(f"  elapsed: {elapsed:.1f}s")
    if stats["gen_seconds"]:
        mean = sum(stats["gen_seconds"]) / len(stats["gen_seconds"])
        print(f"  mean gen time: {mean:.2f}s")
    print(f"  templates: {dict(stats['templates'])}")
    print(f"  plan length distribution: {dict(stats['plan_len_hist'])}")


def _run_curriculum(split: str, spec: List[Tuple[int, int]], args,
                         output_base: Path, base_seed: int,
                         *, per_level_subdirs: bool) -> Dict[int, dict]:
    """Generate a split following a curriculum spec.

    ``spec``: list of ``(level, count)`` tuples (one entry per level).
    ``per_level_subdirs``: True → each level into ``<split>/L{k}/``;
                              False → all levels into flat ``<split>/``.
    Returns ``{level: stats_dict}``.
    """
    out: Dict[int, dict] = {}
    config_offset = getattr(args, "config_offset", 0) or 0
    for level, count in spec:
        if count <= 0:
            continue
        subdir = f"L{level}" if per_level_subdirs else None
        seed = base_seed + level * 7919
        t = time.time()
        # In flat-train mode, restart numbering per level so all configs
        # exist in train/ without collision via the offset.
        if not per_level_subdirs:
            offset = config_offset
        else:
            offset = 0
        if args.num_workers > 1:
            stats = _generate_split_parallel(
                split, count, args, output_base, seed,
                level=level, level_subdir=subdir,
                config_offset=offset,
            )
        else:
            stats = _generate_split(
                split, count, args, output_base, seed,
                level=level, level_subdir=subdir,
                config_offset=offset,
            )
        _print_stats(f"{split}/L{level}", stats, time.time() - t)
        out[level] = stats
        if not per_level_subdirs:
            config_offset += stats["accepted"]
    return out


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--output-dir", type=Path, default=_DEFAULT_OUTPUT_DIR)
    parser.add_argument("--num-train", type=int, default=_DEFAULT_TRAIN)
    parser.add_argument("--num-test", type=int, default=_DEFAULT_TEST)
    parser.add_argument("--num-val", type=int, default=_DEFAULT_VAL)
    parser.add_argument("--templates", nargs="+",
                            default=list(_TEMPLATE_NAMES),
                            choices=_TEMPLATE_NAMES)
    parser.add_argument(
        "--curriculum-spec", type=str, default=None,
        choices=sorted(_CURRICULA.keys()),
        help=("If set, override --num-train/--templates with a curated "
                  "level-mix preset.  Use train_200/train_400/train_600 for "
                  "the train split (flat output), val_per_level / "
                  "test_per_level for validation/test (per-level subdirs)."),
    )
    parser.add_argument(
        "--curriculum-split", type=str, default="train",
        choices=("train", "val", "test", "train_l0_l4"),
        help=("Which split the --curriculum-spec writes to.  ``train`` and "
                  "``train_l0_l4`` use a flat output directory; val/test use "
                  "per-level subdirs.  ``train_l0_l4`` pairs with the curriculum "
                  "spec of the same name (no L5) — used so the OOD train "
                  "dataset lands at <output_dir>/train_l0_l4/ without "
                  "overwriting the in-distribution <output_dir>/train/."),
    )
    parser.add_argument(
        "--level", type=int, default=None, choices=list(range(6)),
        help=("Override curriculum-spec to generate only this level.  "
                  "Used by the per-level shell driver to sidestep the "
                  "fork-pool reuse deadlock observed when one Python "
                  "process drives multiple sequential pools."),
    )
    parser.add_argument(
        "--config-offset", type=int, default=0,
        help=("Shift the starting config_num for filename numbering.  "
                  "Used together with --level so a multi-level dataset "
                  "split across subprocess invocations doesn't collide "
                  "on filenames in a flat output dir."),
    )
    parser.add_argument("--num-workers", type=int, default=_DEFAULT_NUM_WORKERS)
    parser.add_argument("--seed", type=int, default=_DEFAULT_SEED)
    parser.add_argument("--no-viz", action="store_true")
    parser.add_argument(
        "--enable-hand-capsule", action="store_true",
        help=("Leave the Franka hand_capsule bounding-capsule active.  "
                  "Default is DISABLED — the 4cm/6cm capsule's 10cm axial "
                  "extent over-rejects feasible put_upright plans (L4 "
                  "templates fail 100%% with it on).  Disabling falls back "
                  "to the hand_c mesh, which still detects real "
                  "collisions."),
    )
    parser.add_argument("--verbose", action="store_true")
    args = parser.parse_args()

    output_base = args.output_dir.resolve()
    output_base.mkdir(parents=True, exist_ok=True)
    shutil.copy(_DOMAIN_BUNDLED, output_base / "domain.pddl")
    print(f"Output base: {output_base}")
    print(f"Templates: {args.templates}")
    print(f"Workers: {args.num_workers}")

    if args.curriculum_spec is not None:
        spec = _CURRICULA[args.curriculum_spec]
        split = args.curriculum_split
        per_level_subdirs = (split in ("val", "test"))
        # If --level filter is given, run only that level (so a shell
        # driver can dispatch one Python process per level — avoids the
        # fork-pool reuse hang).
        if args.level is not None:
            spec = [(lvl, cnt) for lvl, cnt in spec if lvl == args.level]
        print(f"\nCurriculum preset: {args.curriculum_spec!r}  "
                  f"split={split}  per_level_subdirs={per_level_subdirs}  "
                  f"level_filter={args.level}")
        _run_curriculum(split, spec, args, output_base,
                              args.seed + hash(args.curriculum_spec) % 9999,
                              per_level_subdirs=per_level_subdirs)
        return 0

    splits = [
        ("train", args.num_train),
        ("test", args.num_test),
        ("val", args.num_val),
    ]
    for split, count in splits:
        if count <= 0:
            continue
        t = time.time()
        if args.num_workers > 1:
            stats = _generate_split_parallel(split, count, args, output_base,
                                                      args.seed + hash(split) % 9999)
        else:
            stats = _generate_split(split, count, args, output_base,
                                             args.seed + hash(split) % 9999)
        _print_stats(split, stats, time.time() - t)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
