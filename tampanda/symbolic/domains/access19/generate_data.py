def _state_from_layout(layout: Dict[str, str]) -> Dict[Tuple, bool]:
    """Build the ground-state dict needed by ``restore_state``."""
    return _layout_to_state(layout, held=None)


def _setup_env(scratch_dir: Path):
    """Build env + chains + executor + home pose for a worker."""
    builder, ws, cfg = make_access19_builder(scratch_dir=scratch_dir)
    env = builder.build_env(rate=10000.0)
    apply_runtime_tweaks(env, cfg)
    cube_half = float(env.get_object_half_size("ooi")[2])
    table_z = ws["shelf_interior"].level_z - cube_half
    executor = _build_executor(env, table_z=table_z,
                                       allowed_types=[GraspType.FRONT])
    shelf_home = _solve_access19_staging(env, ws, cfg)
    lik = LinearIKPlanner(env, n_substeps=12, joint_check_steps=8)
    pick_fn = make_access19_pick_fn(env, executor, ws, cfg,
                                            cube_half_z=cube_half, lik=lik)
    put_fn = make_access19_put_fn(env, executor, ws, cfg,
                                          cube_half_z=cube_half, lik=lik)
    return env, ws, cfg, executor, pick_fn, put_fn, shelf_home


# ---------------------------------------------------------------------------
# Plan generation
# ---------------------------------------------------------------------------


def build_plan(
    template: Template,
    env, workspace, cfg,
    executor, pick_fn, put_fn, home_qpos,
    *,
    planner_kind: str,
    time_budget_s: float = 180.0,
) -> Tuple[List[Tuple], Dict[str, Any]]:
    """Dispatch to the right planner for this template/level combination.

    Returns ``(plan, info)`` where ``plan`` is the action sequence and
    ``info`` carries planner-level diagnostics for the meta JSON.
    """
    init_layout = template_source_layout(template)
    goal = template_goal_layout(template)

    if planner_kind == "oracle":
        # OoI-only goal — extract goal cell for OoI.
        if "ooi" not in goal:
            raise ValueError("oracle_plan needs an OoI goal cell")
        goal_cell = Cell.parse(goal["ooi"])
        res = oracle_plan(
            env, workspace, cfg, init_layout, goal_cell, _OBJECT_NAMES,
            pick_fn, put_fn,
            executor=executor, home_qpos=home_qpos, fast=True,
            max_actions=200, max_backtrack=64,
        )
    elif planner_kind == "astar":
        res = astar_plan(
            env, workspace, cfg, init_layout, goal, _OBJECT_NAMES,
            pick_fn, put_fn,
            executor=executor, home_qpos=home_qpos, fast=True,
            max_states=10000, time_budget_s=time_budget_s,
        )
    elif planner_kind == "phased":
        res = phased_plan(
            env, workspace, cfg, init_layout, goal, _OBJECT_NAMES,
            pick_fn, put_fn,
            executor=executor, home_qpos=home_qpos, fast=True,
            phase2_time_budget_s=time_budget_s,
            phase2_max_states=30000,
        )
    else:
        raise ValueError(f"unknown planner_kind {planner_kind!r}")

    info = {
        "planner": planner_kind,
        "success": res.success,
        "plan_len": len(res.plan),
        "n_feasibility_checks": res.n_feasibility_checks,
        "planner_elapsed_s": f"{res.elapsed_s:.2f}",
    }
    return list(res.plan), info


def _sample_template(
    rng: np.random.Generator,
    *,
    level: int,
) -> Template:
    """Sample a template suited to the given curriculum level.

    Always selects from ``_LEVEL_TEMPLATE_NAMES[level]``.  Applies a
    random mirror_x with prob=0.5 for variant multiplication.
    """
    name = str(rng.choice(_LEVEL_TEMPLATE_NAMES[level]))
    return_blockers = level >= 2

    if name == "front_row_subset":
        n = int(rng.integers(0, 2)) if level == 0 \
                else int(rng.integers(2, 4))
        tpl = front_row_subset(n, rng, return_blockers=return_blockers)
    elif name == "dense_front":
        n_rows = int(rng.integers(1, 3))    # 1 or 2 rows
        tpl = dense_front(n_rows, rng, return_blockers=return_blockers)
    elif name == "scattered_subset":
        n = int(rng.integers(3, 7))         # 3..6 scattered blockers
        tpl = scattered_subset(n, rng, return_blockers=return_blockers)
    elif name == "gotcha_corridor_jam":
        tpl = gotcha_corridor_jam(rng, return_blockers=True)
    elif name == "canonical_18":
        tpl = canonical_18(return_blockers=True)
    else:
        raise ValueError(f"unknown template name {name!r}")

    if bool(rng.integers(0, 2)):
        try:
            tpl = mirror_x(tpl)
        except Exception:
            pass        # mirror failed (e.g., collision); use original
    return tpl


# ---------------------------------------------------------------------------
# Per-instance runner
# ---------------------------------------------------------------------------


def _generate_one(
    config_num: int,
    rng: np.random.Generator,
    args,
    env, workspace, cfg,
    executor, pick_fn, put_fn, home_qpos,
    output_dir: Path,
    split: str,
    *,
    level: int,
    level_subdir: Optional[str] = None,
) -> Optional[Dict[str, Any]]:
    """Generate a single access-19 instance for the given curriculum level."""
    template = _sample_template(rng, level=level)
    planner_kind = _LEVEL_PLANNER[level]

    plan, info = build_plan(
        template, env, workspace, cfg,
        executor, pick_fn, put_fn, home_qpos,
        planner_kind=planner_kind,
        time_budget_s=args.time_budget,
    )
    if not info["success"]:
        return None

    # Replay-validate via FAST.
    init_state = _state_from_layout(template_source_layout(template))
    seq_res = check_action_sequence(
        env, workspace, cfg, init_state, plan,
        _OBJECT_NAMES, pick_fn, put_fn,
        executor=executor, fast=True, home_qpos=home_qpos,
        short_circuit=True,
    )
    if not seq_res["success"]:
        return None

    if level_subdir is not None:
        problem_dir = output_dir / split / level_subdir
    else:
        problem_dir = output_dir / split
    problem_dir.mkdir(parents=True, exist_ok=True)

    problem_path = problem_dir / f"config_{config_num}.pddl"
    plan_path = problem_dir / f"config_{config_num}.pddl.plan"
    pn_sub = level_subdir + "-" if level_subdir else ""

    write_pddl_problem(
        template, workspace, problem_path,
        problem_name=f"access19-{split}-{pn_sub}{config_num}",
    )
    write_plan_file(plan, plan_path, metadata={
        "template": template.name,
        "level": level,
        "planner": planner_kind,
        "split": split,
        "config_num": config_num,
        "n_feasibility_checks": info["n_feasibility_checks"],
        "planner_elapsed_s": info["planner_elapsed_s"],
        "replay_elapsed_s": f"{seq_res['elapsed_s']:.2f}",
    })

    return {
        "template": template.name,
        "level": level,
        "planner": planner_kind,
        "plan_len": len(plan),
        "elapsed_s": seq_res["elapsed_s"],
        "config_num": config_num,
        "split": split,
    }


