def _print_stats(split: str, stats: dict, elapsed: float) -> None:
    accepted = stats["accepted"]
    if accepted == 0:
        print(f"\n=== {split} summary ===\n  no instances accepted")
        return
    print(f"\n=== {split} summary ===")
    print(f"  accepted/attempts: {accepted}/{stats['attempts']}")
    print(f"  reject rate: {100 * (1 - accepted/stats['attempts']):.1f}%")
    print(f"  elapsed: {elapsed:.1f}s")
    if stats["gen_seconds"]:
        print(f"  mean gen time: "
                  f"{sum(stats['gen_seconds'])/len(stats['gen_seconds']):.2f}s")
    print(f"  templates: {dict(stats['templates'])}")
    print(f"  plan length distribution: "
              f"{dict(sorted(stats['plan_len_hist'].items()))}")


def _run_curriculum(
    split: str,
    spec: List[Tuple[int, int]],
    args,
    output_base: Path,
    seed: int,
    *,
    flat: bool = False,
    curriculum_spec: Optional[str] = None,
) -> dict:
    """Run a curriculum spec — list of ``(level, count)`` tuples.

    By default each level's instances go to
    ``<output_base>/<split>/L<level>/``.  If ``flat=True``, all
    instances go to ``<output_base>/<split>/`` (collapsed; level
    still recorded in the plan-file metadata).
    """
    overall = _init_stats()
    t_start = time.time()
    offset_per_level = 0
    for level, count in spec:
        print(f"\n[curriculum] {split} L{level}: {count} instances")
        level_subdir = None if flat else f"L{level}"
        stats = _generate_split_parallel(
            split, count, args, output_base, seed + level * 7,
            level=level, level_subdir=level_subdir,
            config_offset=offset_per_level,
            curriculum_spec=curriculum_spec,
        )
        overall["attempts"] += stats["attempts"]
        overall["accepted"] += stats["accepted"]
        overall["reject_plan_failed"] += stats["reject_plan_failed"]
        overall["templates"].update(stats["templates"])
        overall["plan_len_hist"].update(stats["plan_len_hist"])
        overall["gen_seconds"].extend(stats["gen_seconds"])
        offset_per_level += count
    _print_stats(split, overall, time.time() - t_start)
    return overall


