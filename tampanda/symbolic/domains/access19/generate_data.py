def _enumerate_static_init(workspace) -> Tuple[List[str], List[str]]:
    """Return ``(object_names, static_init_predicates)`` for the PDDL :init.

    The access-19 PDDL domain has no static adjacency predicates
    (filter-mode pick/put have only dynamic preconditions), so the
    only "static" thing is the set of objects.  We still emit the
    function for parity with mlb's API.
    """
    cells: List[str] = []
    for region_name in workspace.regions:
        region = workspace[region_name]
        for cell in region.cells():
            cells.append(cell.id)
    objects = list(_OBJECT_NAMES) + cells
    return objects, []


def write_pddl_problem(
    template: Template,
    workspace,
    output_path: Path,
    problem_name: str,
) -> None:
    """Write a PDDL problem file matching the access-19 domain.

    Sections:
      :objects     — every cell + every movable.
      :init        — (occupied cell obj) for each source placement,
                     (empty cell) for the rest, (gripper-empty).
      :goal        — conjunction of (occupied cell obj) over the
                     template's goal_placements.
    """
    cell_names: List[str] = []
    for region_name in workspace.regions:
        region = workspace[region_name]
        for cell in region.cells():
            cell_names.append(cell.id)

    src_layout = template_source_layout(template)
    goal_atoms: List[str] = []
    for obj, cid in template.goal_placements:
        goal_atoms.append(f"(occupied {cid} {obj})")

    occupied_cells = set(src_layout.values())
    init: List[str] = []
    for obj, cid in template.source_placements:
        init.append(f"(occupied {cid} {obj})")
    for c in cell_names:
        if c not in occupied_cells:
            init.append(f"(empty {c})")
    init.append("(gripper-empty)")

    lines: List[str] = []
    lines.append(f"(define (problem {problem_name})")
    lines.append("  (:domain tabletop-access)")
    lines.append("  (:objects")
    lines.append(f"    {' '.join(_OBJECT_NAMES)} - movable")
    lines.append(f"    {' '.join(cell_names)} - cell")
    lines.append("  )")
    lines.append("  (:init")
    for p in init:
        lines.append(f"    {p}")
    lines.append("  )")
    lines.append("  (:goal (and")
    for a in goal_atoms:
        lines.append(f"    {a}")
    lines.append("  ))")
    lines.append(")")
    output_path.write_text("\n".join(lines) + "\n")


def write_plan_file(
    plan: List[Tuple],
    output_path: Path,
    metadata: Dict[str, Any],
) -> None:
    """Write a .pddl.plan file with one action per line + metadata trailer."""
    lines = []
    for action in plan:
        lines.append("(" + " ".join(str(a) for a in action) + ")")
    lines.append(f"; cost = {len(plan)} (unit cost)")
    for k, v in metadata.items():
        lines.append(f"; {k}: {v}")
    output_path.write_text("\n".join(lines) + "\n")


