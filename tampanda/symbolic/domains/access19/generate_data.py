def _worker(worker_id: int, split: str, start_idx: int, count: int,
                 args, output_base_str: str, seed: int,
                 level: int,
                 level_subdir: Optional[str] = None,
                 curriculum_spec: Optional[str] = None,
                 ) -> Tuple[dict, list]:
    """One worker process: generates ``count`` instances at the given level."""
    rng = np.random.default_rng(seed + worker_id * 9999)
    output_base = Path(output_base_str)
    with tempfile.TemporaryDirectory(prefix="access19_data_") as td:
        env, ws, cfg, executor, pick_fn, put_fn, shelf_home = (
            _setup_env(Path(td))
        )
        stats = _init_stats()
        config_num = start_idx
        max_attempts = count * 30
        while stats["accepted"] < count and stats["attempts"] < max_attempts:
            stats["attempts"] += 1
            try:
                res = _generate_one(
                    config_num, rng, args, env, ws, cfg, executor,
                    pick_fn, put_fn, shelf_home,
                    output_base, split,
                    level=level, level_subdir=level_subdir,
                    curriculum_spec=curriculum_spec,
                )
            except Exception:
                stats["reject_plan_failed"] += 1
                continue
            if res is None:
                stats["reject_plan_failed"] += 1
                continue
            stats["accepted"] += 1
            stats["templates"][res["template"]] += 1
            stats["plan_len_hist"][res["plan_len"]] += 1
            stats["gen_seconds"].append(res["elapsed_s"])
            config_num += 1
    return stats, []


def _generate_split_parallel(
    split: str, count: int, args,
    output_base: Path, seed: int,
    *,
    level: int,
    level_subdir: Optional[str] = None,
    config_offset: int = 0,
    curriculum_spec: Optional[str] = None,
) -> dict:
    """Distribute the split across ``args.num_workers`` workers."""
    if args.num_workers <= 1:
        return _generate_split(split, count, args, output_base, seed,
                                       level=level, level_subdir=level_subdir,
                                       config_offset=config_offset,
                                       curriculum_spec=curriculum_spec)

    counts = [count // args.num_workers] * args.num_workers
    for i in range(count % args.num_workers):
        counts[i] += 1
    start_idxs = [config_offset + 1 + sum(counts[:i])
                          for i in range(args.num_workers)]

    ctx = mp.get_context("spawn")
    futures = []
    with ctx.Pool(args.num_workers) as pool:
        for wid in range(args.num_workers):
            if counts[wid] == 0:
                continue
            args_tuple = (wid, split, start_idxs[wid], counts[wid],
                              args, str(output_base), seed,
                              level, level_subdir, curriculum_spec)
            futures.append(pool.apply_async(_worker, args_tuple))
        worker_results = [fut.get() for fut in futures]

    agg = _init_stats()
    for stats, _render in worker_results:
        agg["attempts"] += stats["attempts"]
        agg["accepted"] += stats["accepted"]
        agg["reject_plan_failed"] += stats["reject_plan_failed"]
        agg["templates"].update(stats["templates"])
        agg["plan_len_hist"].update(stats["plan_len_hist"])
        agg["gen_seconds"].extend(stats["gen_seconds"])
    return agg


