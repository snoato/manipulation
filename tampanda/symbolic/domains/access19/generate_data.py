def _copy_domain(output_dir: Path) -> None:
    if _DOMAIN.exists():
        shutil.copy(_DOMAIN, output_dir / "domain.pddl")
    # Bundle the rgnet/GNN variant alongside the planner domain.  The
    # planner uses ``domain.pddl`` (no derived predicates);
    # ``domain_derived.pddl`` carries ``(blocking …)`` derived from
    # the static ``(same-column-front …)`` facts emitted per problem.
    if _DOMAIN_DERIVED.exists():
        shutil.copy(_DOMAIN_DERIVED, output_dir / "domain_derived.pddl")


def main() -> int:
    p = argparse.ArgumentParser(
        description="Generate access-19 PDDL instances + reference plans"
    )
    p.add_argument("--output-dir", type=Path, default=_DEFAULT_OUTPUT_DIR)
    p.add_argument("--num-train", type=int, default=_DEFAULT_TRAIN)
    p.add_argument("--num-val", type=int, default=_DEFAULT_VAL)
    p.add_argument("--num-test", type=int, default=_DEFAULT_TEST)
    p.add_argument("--num-workers", type=int, default=_DEFAULT_NUM_WORKERS)
    p.add_argument("--seed", type=int, default=_DEFAULT_SEED)
    p.add_argument("--time-budget", type=float, default=180.0,
                          help="per-instance planner time budget (s)")
    p.add_argument("--curriculum-spec", choices=list(_CURRICULA.keys()),
                          default=None,
                          help="if set, ignores --num-train and runs a "
                                   "curriculum preset")
    p.add_argument("--level", type=int, default=None,
                          help="run only this level (combine with --num + "
                                   "--split for shell-driver dispatch)")
    p.add_argument("--num", type=int, default=None,
                          help="instances when --level is set")
    p.add_argument("--split", default="train",
                          help="which split to write into when --level is set")
    p.add_argument("--config-offset", type=int, default=0,
                          help="starting config number (for shell-driver "
                                   "dispatch across processes)")
    p.add_argument("--no-domain-copy", action="store_true",
                          help="don't copy domain.pddl to output_dir")
    p.add_argument("--bundle-v2", action="store_true",
                          help="generate the v2 4-dataset bundle "
                                   "(train+val Option-B, eval_pre_b, "
                                   "eval_full).  Output dirs are flat "
                                   "(no L<level>/ subdirs).")
    p.add_argument("--bundle-v4", action="store_true",
                          help="generate the v4 4-dataset bundle "
                                   "(train_600 + val_100 with dense_front_n "
                                   "L4 n∈[10,14] + 50%% return at L3/L4; "
                                   "eval_pre_b + eval_full unchanged). "
                                   "Output dirs are flat.")
    p.add_argument("--bundle-v4-5", action="store_true",
                          help="generate the v4.5 access-19-focused "
                                   "bundle (train_600 + val_10, all "
                                   "dense_front_n with n∈[3,18] uniform "
                                   "and 100%% return; val biases half to "
                                   "canonical_18). Output dirs flat.")
    p.add_argument("--bundle-v4-6", action="store_true",
                          help="generate the v4.6 bundle — same hard "
                                   "section as v4.5 but a GRADED easy tier "
                                   "(easy_ladder, varied OoI placement, "
                                   "20%% no-return for short even-length "
                                   "plans, bucketed by length+return). "
                                   "Output dirs flat.")
    p.add_argument("--verbose", action="store_true")
    args = p.parse_args()

    args.output_dir.mkdir(parents=True, exist_ok=True)
    if not args.no_domain_copy:
        _copy_domain(args.output_dir)

    # Per-level shell-driver mode.
    if args.level is not None:
        if args.num is None:
            print("--level requires --num", flush=True)
            return 2
        level_subdir = f"L{args.level}"
        stats = _generate_split_parallel(
            args.split, args.num, args, args.output_dir, args.seed,
            level=args.level, level_subdir=level_subdir,
            config_offset=args.config_offset,
        )
        _print_stats(f"{args.split}/L{args.level}", stats, 0.0)
        return 0

    # Curriculum-spec mode.
    if args.curriculum_spec is not None:
        spec = _CURRICULA[args.curriculum_spec]
        _run_curriculum("train", spec, args, args.output_dir, args.seed,
                                curriculum_spec=args.curriculum_spec)
        if args.num_val > 0:
            _run_curriculum("val", _CURRICULA["val_per_level"],
                                  args, args.output_dir, args.seed + 100)
        if args.num_test > 0:
            _run_curriculum("test", _CURRICULA["test_per_level"],
                                  args, args.output_dir, args.seed + 200)
        return 0

    # V4.5 bundle mode — access-19-focused full training set.
    # CURATED variant: plan each unique class once, augment via
    # blocker-label permutation + mirror_x.  ~10× fewer phased
    # planner calls than the random-sampling approach the
    # train_600_v4_5 curriculum spec dispatches.
    if args.bundle_v4_5:
        _generate_v4_5_curated(args, args.output_dir, args.seed)
        return 0

    # V4.6 bundle mode — graded easy tier + same hard section as v4.5.
    if args.bundle_v4_6:
        _generate_v4_6_curated(args, args.output_dir, args.seed)
        return 0

    # V4 bundle mode — train_600 + val_100 with dense_front_n L4 +
    # 50% return at L3/L4; eval splits unchanged.
    if args.bundle_v4:
        _run_curriculum("train", _CURRICULA["train_600_v4"],
                                args, args.output_dir, args.seed,
                                flat=True,
                                curriculum_spec="train_600_v4")
        _run_curriculum("val", _CURRICULA["val_100_v4"],
                                args, args.output_dir, args.seed + 100,
                                flat=True,
                                curriculum_spec="val_100_v4")
        _run_curriculum("eval_pre_b", _CURRICULA["eval_30_pre_b"],
                                args, args.output_dir, args.seed + 200,
                                flat=True,
                                curriculum_spec="eval_30_pre_b")
        _run_curriculum("eval_full", _CURRICULA["eval_30_full_a19"],
                                args, args.output_dir, args.seed + 300,
                                flat=True,
                                curriculum_spec="eval_30_full_a19")
        return 0

    # V2 bundle mode — 4 datasets in one shot, flat output dirs.
    if args.bundle_v2:
        # train + val: Option-B distribution (L4 = canonical_12).
        _run_curriculum("train", _CURRICULA["train_300_option_b"],
                                args, args.output_dir, args.seed,
                                flat=True,
                                curriculum_spec="train_300_option_b")
        _run_curriculum("val", _CURRICULA["val_50_option_b"],
                                args, args.output_dir, args.seed + 100,
                                flat=True,
                                curriculum_spec="val_50_option_b")
        # eval_pre_b: original curriculum with canonical_18 at L4.
        _run_curriculum("eval_pre_b", _CURRICULA["eval_30_pre_b"],
                                args, args.output_dir, args.seed + 200,
                                flat=True,
                                curriculum_spec="eval_30_pre_b")
        # eval_full: 30 canonical_18 + return-all (L4 only).
        _run_curriculum("eval_full", _CURRICULA["eval_30_full_a19"],
                                args, args.output_dir, args.seed + 300,
                                flat=True,
                                curriculum_spec="eval_30_full_a19")
        return 0

    print("Specify --curriculum-spec, --bundle-v2, or (--level + --num).",
              flush=True)
    return 2


if __name__ == "__main__":
    raise SystemExit(main())
