def _generate_one(
    config_num: int,
    rng: np.random.Generator,
    args,
    env, workspace, cfg,
    executor, pick_fn, put_fn, home_qpos,
    output_dir: Path,
    split: str,
    *,
    level: int,
    level_subdir: Optional[str] = None,
    curriculum_spec: Optional[str] = None,
) -> Optional[Dict[str, Any]]:
    """Generate a single access-19 instance for the given curriculum level."""
    template = _sample_template(rng, level=level,
                                        curriculum_spec=curriculum_spec)
    planner_kind = _LEVEL_PLANNER[level]
    # When the template is dense_front_n with a small blocker count
    # (n_blockers ≤ 9), astar is much faster than phased and still
    # finds optimal plans.  Matters mainly for v4.5, whose flat
    # n ∈ [3, 18] distribution puts every problem at "L4" → would
    # otherwise dispatch phased even for trivial n=3 cases.
    n_blockers = template.metadata.get("n_blockers")
    if (planner_kind == "phased"
            and isinstance(n_blockers, int) and n_blockers <= 9):
        planner_kind = "astar"

    plan, info = build_plan(
        template, env, workspace, cfg,
        executor, pick_fn, put_fn, home_qpos,
        planner_kind=planner_kind,
        time_budget_s=args.time_budget,
    )
    if not info["success"]:
        return None

    # Replay-validate via FAST.
    init_state = _state_from_layout(template_source_layout(template))
    seq_res = check_action_sequence(
        env, workspace, cfg, init_state, plan,
        _OBJECT_NAMES, pick_fn, put_fn,
        executor=executor, fast=True, home_qpos=home_qpos,
        short_circuit=True,
    )
    if not seq_res["success"]:
        return None

    if level_subdir is not None:
        problem_dir = output_dir / split / level_subdir
    else:
        problem_dir = output_dir / split
    problem_dir.mkdir(parents=True, exist_ok=True)

    problem_path = problem_dir / f"config_{config_num}.pddl"
    plan_path = problem_dir / f"config_{config_num}.pddl.plan"
    pn_sub = level_subdir + "-" if level_subdir else ""

    write_pddl_problem(
        template, workspace, problem_path,
        problem_name=f"access19-{split}-{pn_sub}{config_num}",
    )
    write_plan_file(plan, plan_path, metadata={
        "template": template.name,
        "level": level,
        "planner": planner_kind,
        "split": split,
        "config_num": config_num,
        "n_feasibility_checks": info["n_feasibility_checks"],
        "planner_elapsed_s": info["planner_elapsed_s"],
        "replay_elapsed_s": f"{seq_res['elapsed_s']:.2f}",
    })

    return {
        "template": template.name,
        "level": level,
        "planner": planner_kind,
        "plan_len": len(plan),
        "elapsed_s": seq_res["elapsed_s"],
        "config_num": config_num,
        "split": split,
    }


# ---------------------------------------------------------------------------
# v4.5 curated generation — plan each unique class once, augment via
# blocker-label permutation + mirror_x.  ~10× fewer phased planner
# calls than random sampling for n_blockers ∈ [10, 18].
# ---------------------------------------------------------------------------


_CUBE_BACK_CELLS: Tuple[str, ...] = tuple(
    Cell("shelf_interior", ix, 6).id for ix in (1, 3, 5)
)


_V45_WORKER_STATE: Dict[str, Any] = {}


def _v45_worker_init(base_seed: int) -> None:
    """Per-worker env build for the v4.5 curated pool.  Called once
    per worker process at pool startup."""
    import os
    import tempfile
    scratch = tempfile.TemporaryDirectory(prefix="v4_5_worker_")
    env, ws, cfg, executor, pick_fn, put_fn, shelf_home = _setup_env(
        Path(scratch.name))
    _V45_WORKER_STATE.update({
        "scratch": scratch,
        "env": env, "ws": ws, "cfg": cfg, "executor": executor,
        "pick_fn": pick_fn, "put_fn": put_fn, "shelf_home": shelf_home,
        # Per-worker rng seeded by base + pid for reproducibility +
        # divergence across workers.
        "rng": np.random.default_rng(base_seed + os.getpid()),
    })


def _v45_worker_easy(_task_idx: int) -> Optional[Tuple[Template, List[Tuple]]]:
    """Sample + plan one easy (n=3-9) problem.  ``_task_idx`` is
    ignored — sampling is RNG-driven."""
    s = _V45_WORKER_STATE
    if not s:
        return None
    rng = s["rng"]
    n = int(rng.integers(3, 10))
    tpl = dense_front_n(n, rng, return_blockers=True)
    if bool(rng.integers(0, 2)):
        try:
            tpl = mirror_x(tpl)
        except Exception:
            pass
    plan = _plan_one(tpl, s["env"], s["ws"], s["cfg"], s["executor"],
                        s["pick_fn"], s["put_fn"], s["shelf_home"],
                        planner_kind="astar", time_budget_s=60.0)
    if plan is None:
        return None
    return (tpl, plan)


def _v46_worker_easy(
    _task_idx: int,
) -> Optional[Tuple[Template, List[Tuple], int, bool]]:
    """Sample + plan one v4.6 graded-easy problem.

    20% of samples skip return (one-way blocker moves) so the short
    even-length plans (2/4/6) the strict-return rule can't produce
    become reachable.  Returns ``(template, plan, plan_len,
    return_flag)`` or ``None``.
    """
    s = _V45_WORKER_STATE
    if not s:
        return None
    rng = s["rng"]
    return_blockers = bool(rng.random() >= 0.20)   # 20% no-return
    k = int(rng.integers(0, 3))                     # 0,1,2 blocking
    n_clutter = int(rng.integers(0, 3))             # 0,1,2 clutter
    tpl = easy_ladder(rng, k_blocking=k,
                            return_blockers=return_blockers,
                            n_clutter=n_clutter)
    plan = _plan_one(tpl, s["env"], s["ws"], s["cfg"], s["executor"],
                        s["pick_fn"], s["put_fn"], s["shelf_home"],
                        planner_kind="astar", time_budget_s=120.0)
    if plan is None:
        return None
    return (tpl, plan, len(plan), return_blockers)


def _v45_worker_hard(
    spec: Tuple[str, int, Optional[str]],
) -> Optional[Tuple[Template, List[Tuple]]]:
    """Plan one hard class spec.  ``spec`` is ``(kind, n, ooi_cell)``
    where ``kind ∈ {"dense_front_n", "scattered_subset",
    "canonical_18"}``.  ``ooi_cell`` is used by dense_front_n only.
    """
    s = _V45_WORKER_STATE
    if not s:
        return None
    rng = s["rng"]
    kind, n, ooi_cell = spec
    if kind == "dense_front_n":
        tpl = dense_front_n(n, rng, return_blockers=True,
                                   ooi_cell=ooi_cell)
    elif kind == "scattered_subset":
        tpl = scattered_subset(n, rng, return_blockers=True)
    elif kind == "canonical_18":
        tpl = canonical_18(return_blockers=True)
    else:
        return None
    plan = _plan_one(tpl, s["env"], s["ws"], s["cfg"], s["executor"],
                        s["pick_fn"], s["put_fn"], s["shelf_home"],
                        planner_kind="phased", time_budget_s=300.0)
    if plan is None:
        return None
    return (tpl, plan)


def _random_blocker_perm(
    template: Template, rng: np.random.Generator,
) -> Dict[str, str]:
    """Sample a random non-identity permutation of the template's
    blocker labels.  ``ooi`` is never remapped.  Returns ``{}`` when
    there are fewer than 2 blockers (no meaningful permutation).
    """
    blockers = [obj for obj, _ in template.source_placements
                       if obj.startswith("blocker_")]
    if len(blockers) < 2:
        return {}
    for _attempt in range(8):
        shuffled = list(blockers)
        rng.shuffle(shuffled)
        if shuffled != blockers:
            return dict(zip(blockers, shuffled))
    return {}


def _plan_one(
    tpl: Template, env, ws, cfg, executor, pick_fn, put_fn, shelf_home,
    *, planner_kind: str, time_budget_s: float,
) -> Optional[List[Tuple]]:
    """Plan + FAST-replay-validate a template.  Returns the plan, or
    ``None`` if either step fails.  Mirrors the validation in
    ``_generate_one``."""
    plan, info = build_plan(
        tpl, env, ws, cfg, executor, pick_fn, put_fn, shelf_home,
        planner_kind=planner_kind, time_budget_s=time_budget_s,
    )
    if not info["success"]:
        return None
    init_state = _state_from_layout(template_source_layout(tpl))
    seq_res = check_action_sequence(
        env, ws, cfg, init_state, plan, _OBJECT_NAMES,
        pick_fn, put_fn, executor=executor, fast=True,
        home_qpos=shelf_home, short_circuit=True,
    )
    if not seq_res["success"]:
        return None
    return plan


def _augment_class(
    tpl: Template, plan: List[Tuple],
    rng: np.random.Generator,
    env, ws, cfg, executor, pick_fn, put_fn, shelf_home,
    *, n_perms: int = 5, mirror: bool = True,
) -> List[Tuple[Template, List[Tuple]]]:
    """Produce up to ``n_perms × (2 if mirror else 1)`` (template, plan)
    instances from a single base class.

    Permutations are blocker-label remaps: trivially valid by the
    chain's permutation invariance on object identities → no replay.
    Mirrors flip cell ix → 6 - ix; replay-validated (the chain's IK
    can land in different null-space basins for mirrored configs and
    we want to reject if any substep collides).
    """
    instances: List[Tuple[Template, List[Tuple]]] = [(tpl, list(plan))]
    used_perms: set = set()
    for _ in range(n_perms - 1):
        perm = _random_blocker_perm(tpl, rng)
        if not perm:
            continue
        key = tuple(sorted(perm.items()))
        if key in used_perms:
            continue
        used_perms.add(key)
        rt, rp = permute_blockers(tpl, plan, perm)
        instances.append((rt, rp))

    if not mirror:
        return instances

    mirrored: List[Tuple[Template, List[Tuple]]] = []
    for inst_tpl, inst_plan in instances:
        try:
            mt = mirror_x(inst_tpl)
            mp = mirror_plan_x(inst_plan)
        except Exception:
            continue
        # FAST-replay-validate the mirrored variant.
        init_state = _state_from_layout(template_source_layout(mt))
        try:
            seq_res = check_action_sequence(
                env, ws, cfg, init_state, mp, _OBJECT_NAMES,
                pick_fn, put_fn, executor=executor, fast=True,
                home_qpos=shelf_home, short_circuit=True,
            )
        except Exception:
            continue
        if seq_res.get("success"):
            mirrored.append((mt, mp))
    return instances + mirrored


def _write_problem_and_plan(
    tpl: Template, plan: List[Tuple], ws, output_dir: Path,
    config_num: int, split: str,
) -> None:
    ppath = output_dir / f"config_{config_num}.pddl"
    plpath = output_dir / f"config_{config_num}.pddl.plan"
    write_pddl_problem(
        tpl, ws, ppath,
        problem_name=f"access19-{split}-{config_num}",
    )
    write_plan_file(plan, plpath, metadata={
        "template": tpl.name,
        "n_blockers": tpl.metadata.get("n_blockers", "?"),
        "return": tpl.metadata.get("return", False),
        "permuted": tpl.metadata.get("permuted", False),
        "mirrored": tpl.metadata.get("mirrored", ""),
        "split": split,
        "config_num": config_num,
    })


def _generate_v4_5_curated(
    args, output_dir: Path, seed: int,
) -> int:
    """Curated v4.5 generation.

    Easy path (n ∈ [3, 9]): random sampling × astar → target ~250
    problems.  Each is a unique random instance (no augmentation
    needed — astar is fast).

    Hard path (n ∈ [10, 18]): curated base classes planned once each
    with phased, then augmented via 5 relabellings + mirror_x to give
    up to 10 instances per class.

    Returns total problems written to ``train/``.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    train_dir = output_dir / "train"
    val_dir = output_dir / "val"
    train_dir.mkdir(parents=True, exist_ok=True)
    val_dir.mkdir(parents=True, exist_ok=True)
    _copy_domain(output_dir)

    rng = np.random.default_rng(seed)
    num_workers = max(1, getattr(args, "num_workers", None) or 1)
    print(f"[v4.5 curated] pool size: {num_workers}")

    # --- Section 1: easy (n=3-9) via astar, parallel ------------------
    print(f"[v4.5 curated] Section 1: easy (n=3-9), astar")
    target_easy = 250
    easy_instances: List[Tuple[Template, List[Tuple]]] = []

    with mp.Pool(
        num_workers,
        initializer=_v45_worker_init,
        initargs=(seed,),
    ) as pool:
        # Oversample tasks so we likely hit target_easy even if some
        # samples fail (e.g. mirror collisions, planner timeouts).
        n_tasks = target_easy * 2
        for i, result in enumerate(
            pool.imap_unordered(_v45_worker_easy, range(n_tasks))
        ):
            if result is not None:
                easy_instances.append(result)
                if len(easy_instances) >= target_easy:
                    break
            if (i + 1) % 50 == 0:
                print(f"  easy: {len(easy_instances)}/{target_easy} "
                      f"({i + 1} tasks dispatched)")
        # Pool stays open for Section 2.

        easy_instances = easy_instances[:target_easy]
        print(f"  → {len(easy_instances)} easy instances")

        # --- Section 2: hard base classes (n=10-18), phased, parallel
        print(f"[v4.5 curated] Section 2: hard base classes (n=10-18)")
        hard_specs: List[Tuple[str, int, Optional[str]]] = []
        # n=10..15: 3 dense_front_n + 2 scattered_subset per n
        for n in range(10, 16):
            for ooi_cell in _CUBE_BACK_CELLS:
                hard_specs.append(("dense_front_n", n, ooi_cell))
            for _ in range(2):
                hard_specs.append(("scattered_subset", n, None))
        # n=16,17: 2 dense_front_n per n
        for n in (16, 17):
            for ooi_cell in _CUBE_BACK_CELLS[:2]:
                hard_specs.append(("dense_front_n", n, ooi_cell))
        # n=18: canonical_18
        hard_specs.append(("canonical_18", 18, None))

        print(f"  dispatching {len(hard_specs)} hard classes to "
              f"{num_workers} workers")

        hard_classes: List[Tuple[Template, List[Tuple]]] = []
        for spec, result in zip(
            hard_specs, pool.imap(_v45_worker_hard, hard_specs)
        ):
            if result is None:
                print(f"  [SKIP] {spec}: plan failed")
                continue
            tpl, plan = result
            hard_classes.append((tpl, plan))
            print(f"  {spec}: plan_len={len(plan)}")

    print(f"  → {len(hard_classes)} hard base classes planned")

    # Pool closed.  Build a single env in main process for the
    # mirror-validation phase below.
    scratch = tempfile.TemporaryDirectory(prefix="v4_5_main_")
    env, ws, cfg, executor, pick_fn, put_fn, shelf_home = _setup_env(
        Path(scratch.name))

    # --- Section 3: augment hard classes ---------------------------------
    print(f"[v4.5 curated] Section 3: augment (5 perms × ~2 mirrors)")
    augmented_hard: List[Tuple[Template, List[Tuple]]] = []
    for tpl, plan in hard_classes:
        augments = _augment_class(
            tpl, plan, rng,
            env, ws, cfg, executor, pick_fn, put_fn, shelf_home,
            n_perms=5, mirror=True,
        )
        augmented_hard.extend(augments)
    print(f"  → {len(augmented_hard)} augmented hard instances")

    # --- Section 4: write train ---------------------------------------
    all_train = easy_instances + augmented_hard
    rng.shuffle(all_train)
    for i, (tpl, plan) in enumerate(all_train, start=1):
        _write_problem_and_plan(tpl, plan, ws, train_dir, i, "train")
    print(f"[v4.5 curated] wrote {len(all_train)} train problems")

    # --- Section 5: val (10 problems) ---------------------------------
    val_target = 10
    val_instances: List[Tuple[Template, List[Tuple]]] = []
    # 5 canonical_18 augmentations (identity + 4 relabellings)
    c18_plan = None
    for tpl, plan in hard_classes:
        if tpl.metadata.get("n_blockers") == 18 or "canonical_18" in tpl.name:
            c18_plan = plan
            c18_tpl = tpl
            break
    if c18_plan is not None:
        val_instances.append((c18_tpl, c18_plan))
        used = set()
        while len(val_instances) < 5:
            perm = _random_blocker_perm(c18_tpl, rng)
            if not perm:
                break
            key = tuple(sorted(perm.items()))
            if key in used:
                continue
            used.add(key)
            rt, rp = permute_blockers(c18_tpl, c18_plan, perm)
            val_instances.append((rt, rp))
    # Top up with relabellings of other hard classes (varied n).
    while len(val_instances) < val_target and hard_classes:
        tpl, plan = hard_classes[
            int(rng.integers(0, len(hard_classes)))]
        perm = _random_blocker_perm(tpl, rng)
        if perm:
            rt, rp = permute_blockers(tpl, plan, perm)
            val_instances.append((rt, rp))
        else:
            val_instances.append((tpl, plan))
    val_instances = val_instances[:val_target]
    for i, (tpl, plan) in enumerate(val_instances, start=1):
        _write_problem_and_plan(tpl, plan, ws, val_dir, i, "val")
    print(f"[v4.5 curated] wrote {len(val_instances)} val problems")

    return len(all_train) + len(val_instances)


def _generate_v4_6_curated(
    args, output_dir: Path, seed: int,
) -> int:
    """Curated v4.6 generation.

    Identical hard section to v4.5 (n ∈ [10, 18] curated classes,
    100% return, augmented via perm + mirror), but a GRADED easy
    section that fixes v4.5's degenerate easy tier:

    * varied OoI placement (random column + depth) via easy_ladder
    * 20% of easy problems skip return (one-way blocker moves) so the
      short even-length plans (2/4/6) become reachable
    * bucketed by (plan_len, return_flag) with a per-bucket cap so no
      single structure dominates (v4.5 had 138 identical plans)

    Returns total problems written.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    train_dir = output_dir / "train"
    val_dir = output_dir / "val"
    train_dir.mkdir(parents=True, exist_ok=True)
    val_dir.mkdir(parents=True, exist_ok=True)
    _copy_domain(output_dir)

    rng = np.random.default_rng(seed)
    num_workers = max(1, getattr(args, "num_workers", None) or 1)
    print(f"[v4.6 curated] pool size: {num_workers}")

    # --- Section 1: graded easy, bucketed by (len, return) -----------
    print(f"[v4.6 curated] Section 1: graded easy (easy_ladder)")
    target_easy = 160
    cap_per_bucket = 25       # cap so no (len, return) class dominates
    max_easy_len = 10         # anything longer isn't "easy"
    easy_buckets: Dict[Tuple[int, bool], List[Tuple[Template, List[Tuple]]]] = (
        collections.defaultdict(list))

    with mp.Pool(
        num_workers,
        initializer=_v45_worker_init,
        initargs=(seed,),
    ) as pool:
        n_tasks = target_easy * 6     # oversample — buckets + caps reject many
        n_easy = 0
        for i, result in enumerate(
            pool.imap_unordered(_v46_worker_easy, range(n_tasks))
        ):
            if result is not None:
                tpl, plan, plan_len, ret = result
                if plan_len <= max_easy_len:
                    key = (plan_len, ret)
                    if len(easy_buckets[key]) < cap_per_bucket:
                        easy_buckets[key].append((tpl, plan))
                        n_easy += 1
            if n_easy >= target_easy:
                break
            if (i + 1) % 100 == 0:
                print(f"  easy: {n_easy}/{target_easy} "
                      f"({i + 1} tasks); buckets="
                      f"{ {k: len(v) for k, v in sorted(easy_buckets.items())} }")

        easy_instances = [it for v in easy_buckets.values() for it in v]
        print(f"  → {len(easy_instances)} easy instances; "
              f"length×return buckets: "
              f"{ {k: len(v) for k, v in sorted(easy_buckets.items())} }")

        # --- Section 2: hard base classes (IDENTICAL to v4.5) --------
        print(f"[v4.6 curated] Section 2: hard base classes (n=10-18)")
        hard_specs: List[Tuple[str, int, Optional[str]]] = []
        for n in range(10, 16):
            for ooi_cell in _CUBE_BACK_CELLS:
                hard_specs.append(("dense_front_n", n, ooi_cell))
            for _ in range(2):
                hard_specs.append(("scattered_subset", n, None))
        for n in (16, 17):
            for ooi_cell in _CUBE_BACK_CELLS[:2]:
                hard_specs.append(("dense_front_n", n, ooi_cell))
        hard_specs.append(("canonical_18", 18, None))

        print(f"  dispatching {len(hard_specs)} hard classes to "
              f"{num_workers} workers")
        hard_classes: List[Tuple[Template, List[Tuple]]] = []
        for spec, result in zip(
            hard_specs, pool.imap(_v45_worker_hard, hard_specs)
        ):
            if result is None:
                print(f"  [SKIP] {spec}: plan failed")
                continue
            tpl, plan = result
            hard_classes.append((tpl, plan))
            print(f"  {spec}: plan_len={len(plan)}")

    print(f"  → {len(hard_classes)} hard base classes planned")

    scratch = tempfile.TemporaryDirectory(prefix="v4_6_main_")
    env, ws, cfg, executor, pick_fn, put_fn, shelf_home = _setup_env(
        Path(scratch.name))

    # --- Section 3: augment hard classes (IDENTICAL to v4.5) ---------
    print(f"[v4.6 curated] Section 3: augment (5 perms × ~2 mirrors)")
    augmented_hard: List[Tuple[Template, List[Tuple]]] = []
    for tpl, plan in hard_classes:
        augmented_hard.extend(_augment_class(
            tpl, plan, rng,
            env, ws, cfg, executor, pick_fn, put_fn, shelf_home,
            n_perms=5, mirror=True,
        ))
    print(f"  → {len(augmented_hard)} augmented hard instances")

    # --- Section 4: write train --------------------------------------
    all_train = easy_instances + augmented_hard
    rng.shuffle(all_train)
    for i, (tpl, plan) in enumerate(all_train, start=1):
        _write_problem_and_plan(tpl, plan, ws, train_dir, i, "train")
    print(f"[v4.6 curated] wrote {len(all_train)} train problems")

    # --- Section 5: val (10 problems, canonical_18-biased) -----------
    val_target = 10
    val_instances: List[Tuple[Template, List[Tuple]]] = []
    c18_plan = None
    c18_tpl = None
    for tpl, plan in hard_classes:
        if tpl.metadata.get("n_blockers") == 18 or "canonical_18" in tpl.name:
            c18_plan, c18_tpl = plan, tpl
            break
    if c18_plan is not None:
        val_instances.append((c18_tpl, c18_plan))
        used = set()
        while len(val_instances) < 5:
            perm = _random_blocker_perm(c18_tpl, rng)
            if not perm:
                break
            key = tuple(sorted(perm.items()))
            if key in used:
                continue
            used.add(key)
            rt, rp = permute_blockers(c18_tpl, c18_plan, perm)
            val_instances.append((rt, rp))
    while len(val_instances) < val_target and hard_classes:
        tpl, plan = hard_classes[int(rng.integers(0, len(hard_classes)))]
        perm = _random_blocker_perm(tpl, rng)
        if perm:
            rt, rp = permute_blockers(tpl, plan, perm)
            val_instances.append((rt, rp))
        else:
            val_instances.append((tpl, plan))
    val_instances = val_instances[:val_target]
    for i, (tpl, plan) in enumerate(val_instances, start=1):
        _write_problem_and_plan(tpl, plan, ws, val_dir, i, "val")
    print(f"[v4.6 curated] wrote {len(val_instances)} val problems")

    return len(all_train) + len(val_instances)


